/**
 * 只有英雄才有的屬性
 * UE4:AHeroCharacter
 *
 * @class Hero
 */
class HeroCharacter {

  /**
   * 力量
   */
  Strength = 0

  /**
   * 敏捷
   */
  Agility = 0

  /**
   * 智力
   */
  Intelligence = 0

  /**
   * 外加力量
   */
  AdditionStrength = 0

  /**
   * 外加敏捷
   */
  AdditionAgility = 0


  /**
   * 外加智力
   */
  AdditionIntelligence = 0

  /**
   * 復活倒數
   */
  DeadTime = 0

  /**
   * 死亡給敵經驗值
   */
  BountyEXP = 0

  /**
   * 等級
   */
  CurrentLevel = 0

  /**
   * 目前經驗值
   */
  CurrentEXP = 0
}

export default HeroCharacter
